class Ticket {

  final String from;
  final String to;
  final double pennyPrice;
  final double duration;
  final String departure;
  final String arrival;
  final String key;
  final String color;
  final String sourceLocation;
  final String destLocation;

  Ticket(this.from,
      this.to,
      this.pennyPrice,
      this.duration,
      this.departure,
      this.arrival,
      this.key,
      this.color,
      this.sourceLocation,
      this.destLocation);
}